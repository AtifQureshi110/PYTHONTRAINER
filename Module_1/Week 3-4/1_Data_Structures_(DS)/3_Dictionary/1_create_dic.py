# Empty dictionary
my_dict = {}
print(my_dict,"my_dict")

# Dictionary with integer keys
int_keys = {1: "One", 2: "Two", 3: "Three"}
print(int_keys, "int_keys")

# Dictionary with mixed keys
mixed_keys = {"name": "Alice", 1: [2, 3, 4]}
print(mixed_keys,"mixed_keys")

# Nested dictionary
nested_dict = {"course": "Python", "details": {"duration": "3 months", "level": "Beginner"}}
print(nested_dict,"nested_dict")