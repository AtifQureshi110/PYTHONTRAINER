my_list = [1, 2, 3]

# Append an element
my_list.append(4)
print("This will add the item at end of the list.\n",my_list)

# Insert an element at a specific index
my_list.insert(1, 10)
print("This will add or insert the item at index number 1 of the list.\n",my_list)  

# Extend the list with another list
my_list.extend([5, 6])
print("This will add a new list in the existing list.\n",my_list) 
